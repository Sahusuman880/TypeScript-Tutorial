//we can create a array follwoing ways

//1.Using Square Bracket

const number1:number[]=[1,2,3,4,5,6];
console.log(number1[3]);

//2.Using Array Constructor
const number2:number[]=new Array(1,2,3,4,5,6);
console.log(number2[4]);

//3.Using Array.of method
const number3=Array.of(1,2,3,4,5,6);
console.log(number3[2]);
